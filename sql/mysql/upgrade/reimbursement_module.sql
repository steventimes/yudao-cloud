CREATE TABLE IF NOT EXISTS `reimbursement_claim` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '编号',
  `reimbursement_no` varchar(64) NULL DEFAULT NULL COMMENT '报销单号',
  `user_id` bigint NOT NULL COMMENT '申请人用户编号',
  `dept_id` bigint NULL DEFAULT NULL COMMENT '申请人部门编号快照',
  `applicant_name_snapshot` varchar(64) NOT NULL DEFAULT '' COMMENT '申请人名称快照',
  `reason` varchar(512) NOT NULL DEFAULT '' COMMENT '报销事由',
  `total_amount` decimal(24, 6) NOT NULL DEFAULT 0 COMMENT '报销总金额',
  `currency` varchar(8) NOT NULL DEFAULT 'CNY' COMMENT '币种',
  `status` tinyint NOT NULL COMMENT '报销业务状态',
  `process_instance_id` varchar(64) NULL DEFAULT NULL COMMENT 'BPM 流程实例编号',
  `source` varchar(16) NOT NULL DEFAULT 'MANUAL' COMMENT '来源',
  `mailbox_connection_id` bigint NULL DEFAULT NULL COMMENT '来源邮箱绑定编号',
  `ai_confidence` decimal(5, 4) NULL DEFAULT NULL COMMENT 'AI 总体置信度',
  `ai_failure_message` varchar(512) NULL DEFAULT NULL COMMENT 'AI 失败原因',
  `submit_time` datetime NULL DEFAULT NULL COMMENT '提交时间',
  `creator` varchar(64) NULL DEFAULT '' COMMENT '创建者',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` varchar(64) NULL DEFAULT '' COMMENT '更新者',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` bit(1) NOT NULL DEFAULT b'0' COMMENT '是否删除',
  `tenant_id` bigint NOT NULL DEFAULT 0 COMMENT '租户编号',
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `uk_reimbursement_claim_no` (`tenant_id`, `reimbursement_no`) USING BTREE,
  KEY `idx_reimbursement_claim_user` (`tenant_id`, `user_id`, `status`) USING BTREE,
  KEY `idx_reimbursement_claim_process` (`process_instance_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='报销申请';

