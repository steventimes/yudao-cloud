  `amount` decimal(24, 6) NOT NULL COMMENT '金额',
  `tax_amount` decimal(24, 6) NULL DEFAULT NULL COMMENT '税额',
  `invoice_number` varchar(128) NULL DEFAULT NULL COMMENT '发票号码',
